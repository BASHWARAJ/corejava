package com.gviet.arithmaticoperation;

 class ArithmaticOperation {
	    int operand1;
	    int operand2;
	    //constructor is a special method()
	     ArithmaticOperation() {

	        operand1=10;
	        operand2=20;
	    
	}
	  // addition method
	 void addTwoNumbers() {
	    System.out.println(operand1+operand2);
	}
	 //subtraction method

	void subTwoNumbers() {
	    System.out.println(operand1-operand2);
	}
	    //multiplication method

	 void multiplyTwoNumbers() {
	    System.out.println(operand1*operand2);
	}


	 void divideTwoNumbers() {
	    System.out.println(operand1/operand2);


	}

}
